Cypress.Commands.add('login', (nome, senha) => {
    cy.visit("http://processos.bdrsistemas.com.br/")
    cy.get('[id="input_email"]').type(nome);
    cy.get('[id="input_senha"]').type(senha,{log:false});
    cy.get('button[type="button"]').click();
})
